                              </li>
                                    <li>Bethabara Info
									<ul class="trailInfo" >
   			    											
                                                            <li><span class="highlight">Length:</span> 2.7 miles, part of a 9-mile network of woodland trails and wetland boardwalks through Historic Bethabara Park (HBP).</li>
                                                            <li><span class="highlight">End points: </span>Crowne Oaks Apartments to Reynolda Commons Shopping Center.</li>
                                                            <li><span class="highlight">Surface: </span>Paved between Old Town Road and Old Town Drive, and from Midkiff Road to Reynolda Road; otherwise, a variety of unpaved surfaces.</li>
                                                            <li><span class="highlight">Terrain:</span> Paved sections are flat.</li>
                                                             <li><span class="highlight">Navigation:</span> Bethabara Greenway is bisected by Old Town Drive. Pedestrians and cyclists should use extreme caution when crossing. There are blind curves in both directions, and currently no crosswalk or sidewalk. The City of Winston-Salem does have plans to upgrade this dangerous intersection.</li> 
                                                            <li><span class="highlight">Parking:</span> Paved and gravel lots at HBP Visitor Center; unmarked gravel lots on Old Town Drive; unmarked on-street parking at the end of Midkiff Road and at the end of Indiana Avenue  Ext.; unmarked paved parking at Reynolda Commons Shopping Center (from there greenway access is down a set of wooden steps on Reynolda Road). </li> 
                                                            <li><span class="highlight">Amenities:</span> Benches, trash cans, bulletin boards, nature and historical interpretive signage.</li>
                                                             <li><span class="highlight">Nearby amenities:</span> At HBP Visitor Center (open 10:30-4:30 Tue.-Fri., 1:30-4:30 Sat. and Sun.): restrooms, water fountain, maps of trails. At Bethabara City Park (parking off Old Town Drive): portable toilet, playground, parcourse and picnic area with grill.</li>
                                                            <li><span class="highlight">Connects to:</span> One paved section of the Bethabara Greenway runs behind HBP's restored 18th century church and potter's house, archaeological ruins, reconstructed palisade fort and gardens. Reynolds Commons Shopping Center, at the Reynolda Road end of the greenway, offers a grocery, pharmacy, restaurants and other businesses.</li>
