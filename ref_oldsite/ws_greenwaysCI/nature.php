  <p>Greenways create or preserve a ribbon of nature right in the city. Most are located along a creek, stream or lake, providing a green corridor that improves water quality and creates a healthy environment for wildlife. Greenways slow us down so that we can take in the plants, animals, woods, water and sky we so often miss while whizzing around in our cars. </p>
                    <p>Winston-Salem's greenways incorporate a wide variety of habitats ranging from mature hardwood forest to open fields to marshes and wetlands. An early-morning visit on any greenway might include an encounter with grazing rabbits or a deer. Butterflies are attracted to the native wildflowers growing along the unmown edges of the paths. Beavers can sometimes be seen in the wetland by Bethabara Greenway or along Muddy Creek.</p>
                    <p>The greenways offer something special in each season. In early spring, Bethabara's trail-side vernal pools are home to scores of loud Chorus Frogs. In spring and summer, bluebirds raise their young in the many nesting boxes set up along Muddy Creek, and on winter evenings, flocks of geese fly through colorful sunsets to feed in adjacent fields.   </p>
                    <span class="imgWrapRight"><img src="images/photos/nature/baltimore4.jpg" width="415" height="310" /><span class="clear"></span></span>
                    <p class="captionRight">Baltimore Oriole</p>
                    <p>Local birdwatchers are particularly fond of greenways because of the access they give to habitats for resident and migrating birds. The Bethabara Greenway is a mecca for colorful warblers feeding on insects and berries during fall migration. Even small Civitan Park along Salem Creek Greenway provides nesting habitat for several locally uncommon species. Forsyth Audubon's Second Saturday Bird Walks often visit greenways and are open to anyone. Check the <a href="http://www.forsythaudubon.org/" target="_blank">FA website</a> for information.</p>
                    

